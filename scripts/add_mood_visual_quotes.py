def find_files(root: Path):
    files = []
    for p in root.rglob('*.md'):
        if p.name == 'README.md':
            continue
        rel = p.relative_to(root).as_posix()
        files.append(rel)
    return sorted(files)


def parse_frontmatter(text: str):
    m = re.match(r'---\n(.*?)\n---\n', text, re.DOTALL)
    if not m:
        return None, None, text
    return m.group(1), m.group(0), text[m.end():]


def insert_mood_in_frontmatter(fm_text: str, mood: str):
    """在 tags: 之后插入 mood:，保持原有格式尽量不变。"""
    # 先解析再重新组装，保证 YAML 安全
    data = yaml.safe_load(fm_text)
    if data is None:
        data = {}

    # 如果已有 mood，仅当新值不同时才覆盖
    data['mood'] = mood

    # 重建有序 frontmatter：保持原 key 顺序，仅把 mood 放到 tags 之后
    lines = fm_text.splitlines()
    tag_line_idx = None
    for i, line in enumerate(lines):
        if re.match(r'^tags:\s*', line):
            tag_line_idx = i
            # 如果 tags 是多行列表，找到最后一行
            j = i + 1
            while j < len(lines) and (lines[j].startswith('- ') or lines[j].strip() == ''):
                # 空行可能结束列表，也可能只是间隔；保守处理：遇到非 - 且非空行则停止
                if lines[j].strip() == '':
                    j += 1
                    continue
                j += 1
            # 找到 tags 值结束位置（最后一个以 '- ' 开头的行）
            last_tag_idx = i
            for k in range(i + 1, len(lines)):
                if lines[k].startswith('- '):
                    last_tag_idx = k
                elif lines[k].strip() == '':
                    continue
                else:
                    break
            tag_line_idx = last_tag_idx
            break

    # 移除已有的 mood 行
    new_lines = []
    skip = False
    for line in lines:
        if re.match(r'^mood:\s*', line):
            skip = True
            continue
        if skip and line.startswith(' '):
            continue
        skip = False
        new_lines.append(line)

    # 在 tags 后插入
    insert_idx = tag_line_idx + 1 if tag_line_idx is not None else len(new_lines)
    new_lines.insert(insert_idx, f'mood: {mood}')

    return '\n'.join(new_lines) + '\n'


def add_emotion_visual_section(body: str, emotional_tone: str, visual_image: str):
    """在 ## 自媒体金句 板块末尾、下一个 ## 之前插入 ### 情绪与视觉。"""
    if '### 情绪与视觉' in body:
        # 已存在则替换内容
        pattern = re.compile(
            r'(## 自媒体金句\n.*?)(### 情绪与视觉\n.*?)(?=\n## |\Z)',
            re.DOTALL
        )
        new_section = f'### 情绪与视觉\n\n- **情绪基调**：{emotional_tone}\n- **视觉意象**：{visual_image}\n'
        body = pattern.sub(r'\1' + new_section, body, count=1)
        return body

    # 找到 ## 自媒体金句 位置
    match = re.search(r'## 自媒体金句\n', body)
    if not match:
        return body

    start = match.start()
    # 找到该 section 的结束：下一个 ## 标题或文件末尾
    next_heading = re.search(r'\n## ', body[start + len('## 自媒体金句\n'):])
    if next_heading:
        section_end = start + len('## 自媒体金句\n') + next_heading.start()
    else:
        section_end = len(body)

    section = body[start:section_end]
    # 确保 section 末尾有换行
    if not section.endswith('\n'):
        section += '\n'
    # 如果末尾已经有多个换行，保留一个
    section = section.rstrip('\n') + '\n\n'

    new_subsection = f'### 情绪与视觉\n\n- **情绪基调**：{emotional_tone}\n- **视觉意象**：{visual_image}\n\n'
    new_section = section + new_subsection

    body = body[:start] + new_section + body[section_end:]
    return body


def process_file(rel_path: Path, mood: str, emotional_tone: str, visual_image: str):
    p = ROOT / rel_path
    text = p.read_text(encoding='utf-8')

    fm_text, fm_full, body = parse_frontmatter(text)
    if fm_text is None:
        print(f'  [跳过] 无 frontmatter: {rel_path}')
        return False

    new_fm_text = insert_mood_in_frontmatter(fm_text, mood)
    new_body = add_emotion_visual_section(body, emotional_tone, visual_image)

    # 验证 YAML 可解析
    try:
        yaml.safe_load(new_fm_text)
    except yaml.YAMLError as e:
        print(f'  [错误] YAML 解析失败: {rel_path}: {e}')
        return False

    new_text = f'---\n{new_fm_text}---\n{new_body}'
    p.write_text(new_text, encoding='utf-8')
    return True


def main():
    files = find_files(ROOT)
    total = len(files)
    missing_mapping = []
    updated = 0
    skipped = 0

    for rel in files:
        if rel not in QUOTE_MOOD_VISUAL:
            missing_mapping.append(rel)
            continue
        mood, emotional_tone, visual_image = QUOTE_MOOD_VISUAL[rel]
        try:
            if process_file(rel, mood, emotional_tone, visual_image):
                updated += 1
            else:
                skipped += 1
        except Exception as e:
            print(f'  [错误] 处理 {rel} 失败: {e}')
            skipped += 1

    print(f'\n处理完成：')
    print(f'  总文件数: {total}')
    print(f'  已更新: {updated}')
    print(f'  跳过/失败: {skipped}')
    if missing_mapping:
        print(f'  缺少映射: {len(missing_mapping)}')
        for m in missing_mapping:
            print(f'    - {m}')


if __name__ == '__main__':
    main()
